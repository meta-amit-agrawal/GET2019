
public class Point 
{
	double xPoint;
	double yPoint;
	
	//parameterized constructor to initialize the  x and y coordinate of the point
	public Point(double x , double y)
	{
		xPoint = x;
		yPoint = y;
	}

	//returns the x coordinate 
	public double getxPoint() {
		
		return xPoint;
		
	}

	//returns the y coordinate
	public double getyPoint() {
		
		return yPoint;
		
	}
	
	
}
